    private void butActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butActualizarActionPerformed
        String txtcod = txtCodigo.getText();
        String descripcion = txtDescripcion.getText();
        String txtprecio = txtPrecio.getText();
        String rubro = (String) boxRubro.getSelectedItem();
        int stock = (int) spiStock.getValue();
        if (txtcod.isEmpty() || descripcion.isEmpty() || txtprecio.isEmpty() || rubro.isEmpty() || stock == 0) {
            JOptionPane.showMessageDialog(this, "No hay datos cargados o los datos son erroneos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int codigo = Integer.parseInt(txtcod);
                double precio = Double.parseDouble(txtprecio);
                for (Producto p : productos) {
                    if (p.getCodigo() == codigo) {
                        p.setDescripcion(descripcion);
                        p.setPrecio(precio);
                        p.setStock(stock);
                        p.setRubro(rubro);
                        p.setStock(stock);
                        JOptionPane.showMessageDialog(this, "Producto modificado exitosamente!", "Producto modificado", JOptionPane.INFORMATION_MESSAGE);
                        butGuardar.setEnabled(true);
                    }

                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El codigo y el precio debe tener un valor numerico", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_butActualizarActionPerformed

