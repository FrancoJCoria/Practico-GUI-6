    private void butNuevoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butNuevoMouseClicked
        String txtC = txtCodigo.getText();
        int stock = (int) spiStock.getValue();
        if (txtCodigo.getText().isEmpty() || txtDescripcion.getText().isEmpty() || txtPrecio.getText().isEmpty() || stock == 0) {
            JOptionPane.showMessageDialog(this, "No hay datos cargados o datos erroneos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                boolean codigoOcupado = false;
                int codigo = Integer.parseInt(txtC);
                for (Producto p : productos) {
                    if (p.getCodigo() == codigo) {
                        JOptionPane.showMessageDialog(this, "El codigo ya le pertenece a un producto", "Error", JOptionPane.ERROR_MESSAGE);
                        codigoOcupado = true;
                    }
                }

                if (codigoOcupado == false) {
                    String descripcion = txtDescripcion.getText();
                    String txtP = txtPrecio.getText();
                    double precio = Double.parseDouble(txtP);
                    String rubro = (String) boxRubro.getSelectedItem();
                    Producto nuevop = new Producto(codigo, descripcion, precio, stock, rubro);
                    productos.add(nuevop);

                    JOptionPane.showMessageDialog(this, "Producto agregado exitosamente!", "Producto agregado", JOptionPane.INFORMATION_MESSAGE);
                    butGuardar.setEnabled(true);
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El codigo y el precio debe tener un valor numerico", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_butNuevoMouseClicked

