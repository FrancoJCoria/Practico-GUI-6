    public void actualizarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        for (Producto p : productos) {
            if (boxCategoria.getSelectedItem().equals(p.getRubro())) {
                Object[] fila = {p.getCodigo(), p.getDescripcion(), p.getPrecio(), p.getRubro(), p.getStock()};
                modelo.addRow(fila);
            }
        }

    }

